#include <stdlib.h>

void	free_av(char ***av)
{
	char	**ptr;

	ptr = *av;
	while (*ptr)
	{
		free(*ptr);
		*ptr = NULL;
		ptr++;
	}
	free(*av);
	*av = NULL;
}
